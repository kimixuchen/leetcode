/**
 * Definition for binary tree
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode(int x) : val(x), left(NULL), right(NULL) {}
 * };
 */
class BSTIterator {
private:
    stack<TreeNode*> s;
    void refresh(TreeNode *cur)
    {
        TreeNode* tmp = cur;
        while(tmp!=NULL)
        {
            s.push(tmp);
            tmp = tmp->left;
        }
    }

public:
    BSTIterator(TreeNode *root) {
        refresh(root);
    }

    /** @return whether we have a next smallest number */
    bool hasNext() {
        return !s.empty();
    }

    /** @return the next smallest number */
    int next() {
        TreeNode* cur = s.top();
        s.pop();
        refresh(cur->right);
        return cur->val;
    }
};

